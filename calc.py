## calculator for 2 numbers


inp_1 = int(input("1) "))
func = input('enumerate:, +, -, *,/ \n' )
inp_2 = int(input("2) "))
if func == '+':
    print(inp_1 + inp_2)
    
elif func == '*':
    print(inp_1 *inp_2)
    
elif func == '-':
    print(inp_1-inp_2)
    
elif func == '/':
    print(inp_1/inp_2)
    
else:
    print("Please input a numbers")